  static Map<String, int> _buildWordFreq(List<List<String>> wordsPerSentence) {
    final freq = <String, int>{};
    for (final words in wordsPerSentence) {
      for (final w in words) {
        freq[w] = (freq[w] ?? 0) + 1;
      }
    }
    return freq;
  }

  static List<double> _calcSentenceWeights(
