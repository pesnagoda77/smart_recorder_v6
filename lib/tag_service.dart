  /// Извлекает теги из текста транскрипции
  static List<String> extractTags(String text, {int maxTags = 5}) {
    if (text.isEmpty) return [];

    // Приводим к нижнему регистру, убираем пунктуацию
    final cleaned = text
        .toLowerCase()
        .replaceAll(RegExp(r'[^\s\w\u0400-\u04FF]'), ' ')
        .replaceAll(RegExp(r'\s+'), ' ')
        .trim();

    if (cleaned.isEmpty) return [];

    // Разбиваем на слова
    final words = cleaned.split(' ');

    // Фильтруем стоп-слова и короткие слова (менее 3 букв)
    final filtered = words.where((w) {
      return w.length >= 3 && !_stopWords.contains(w);
    }).toList();

    // Считаем частоту
    final freq = <String, int>{};
    for (final w in filtered) {
      freq[w] = (freq[w] ?? 0) + 1;
    }

    // Сортируем по частоте, берём top N
    final sorted = freq.entries.toList()
      ..sort((a, b) => b.value.compareTo(a.value));

    return sorted.take(maxTags).map((e) => e.key).toList();
  }

  /// Проверяет, содержит ли текст ключевые слова (для поиска)
  static bool containsKeywords(String text, List<String> keywords) {
    final lowerText = text.toLowerCase();
    return keywords.any((k) => lowerText.contains(k.toLowerCase()));
  }
